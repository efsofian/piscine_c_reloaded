#include <stdlib.h>

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	char	*new;
	int		i;
	int		src_len;

	i = 0;
	if (!src)
		return (NULL);
	src_len = ft_strlen(src);
	if (!(new = (char *)malloc(sizeof(char) * (src_len + 1))))
		return (NULL);
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
